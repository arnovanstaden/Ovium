// Animations

$(".contact-info a:nth-child(2)").hover(function() {
    $(".contact-info a:nth-child(2) i").toggleClass("fa-envelope")
    $(".contact-info a:nth-child(2) i").toggleClass("fa-envelope-open")
});

$(".contact-info a:nth-child(3)").hover(function() {
    $(".contact-info a:nth-child(3) i").toggleClass("fa-map-marker-alt")
    $(".contact-info a:nth-child(3) i").toggleClass("fa-map-marked-alt")
})